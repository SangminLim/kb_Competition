INSERT INTO TB_DEDUCTION_MONTHLY_SUMMARY (
    CUSTOMER_ID,
    BASE_YM,
    MERCHANT_TYPE,
    TOTAL_AMT,
    TXN_COUNT,
    REG_DTM
)
SELECT
    S.CUSTOMER_ID,
    S.BASE_YM,
    S.MERCHANT_TYPE,
    S.TOTAL_AMT,
    S.TXN_COUNT,
    NOW() AS REG_DTM
FROM (
SELECT
    A.CUSTOMER_ID AS CUSTOMER_ID,
    A.BASE_YM AS BASE_YM,
    CASE
    WHEN J1.MERCHANT_ID IS NOT NULL THEN 'BOOK_PERF'
    WHEN J2.MERCHANT_ID IS NOT NULL THEN 'TRAD_MARKET'
    WHEN J3.MERCHANT_ID IS NOT NULL THEN 'GENERAL'
    ELSE 'UNKNOWN'
END AS MERCHANT_TYPE,
    SUM(A.SALES_AMT) AS TOTAL_AMT,
    COUNT(*) AS TXN_COUNT
FROM TB_CARD_SALES_LEDGER A
LEFT JOIN TB_BOOK_PERF_MERCHANT J1
  ON A.MERCHANT_ID = J1.MERCHANT_ID AND A.SALES_DT BETWEEN J1.APPLY_START_DT AND IFNULL(J1.APPLY_END_DT, '99991231')
 AND J1.USE_YN = 'Y'
LEFT JOIN TB_TRAD_MARKET_MERCHANT J2
  ON A.MERCHANT_ID = J2.MERCHANT_ID AND A.SALES_DT BETWEEN J2.APPLY_START_DT AND IFNULL(J2.APPLY_END_DT, '99991231')
 AND J2.USE_YN = 'Y'
LEFT JOIN TB_GENERAL_DEDUCT_MERCHANT J3
  ON A.MERCHANT_ID = J3.MERCHANT_ID AND A.SALES_DT BETWEEN J3.APPLY_START_DT AND IFNULL(J3.APPLY_END_DT, '99991231')
 AND J3.USE_YN = 'Y'
WHERE A.BASE_YM = :base_ym
  AND A.CANCEL_YN = 'N'
  AND (J1.MERCHANT_ID IS NOT NULL OR J2.MERCHANT_ID IS NOT NULL OR J3.MERCHANT_ID IS NOT NULL)
GROUP BY
    A.CUSTOMER_ID,
    A.BASE_YM,
    CASE
    WHEN J1.MERCHANT_ID IS NOT NULL THEN 'BOOK_PERF'
    WHEN J2.MERCHANT_ID IS NOT NULL THEN 'TRAD_MARKET'
    WHEN J3.MERCHANT_ID IS NOT NULL THEN 'GENERAL'
    ELSE 'UNKNOWN'
END
) S
