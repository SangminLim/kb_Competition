SELECT
    L.SALES_SEQ_NO AS SALES_SEQ_NO,
    L.SALES_DT AS SALES_DT,
    L.CUSTOMER_ID AS CUSTOMER_ID,
    L.MERCHANT_ID AS MERCHANT_ID,
    L.SALES_AMT AS SALES_AMT,
    L.BASE_YM AS BASE_YM,
    CASE
    WHEN J1.MERCHANT_ID IS NOT NULL THEN 'BOOK_PERF'
    WHEN J2.MERCHANT_ID IS NOT NULL THEN 'TRAD_MARKET'
    WHEN J3.MERCHANT_ID IS NOT NULL THEN 'GENERAL'
    ELSE 'UNKNOWN'
END AS MERCHANT_TYPE
FROM TB_CARD_SALES_LEDGER L
LEFT JOIN TB_BOOK_PERF_MERCHANT J1
  ON L.MERCHANT_ID = J1.MERCHANT_ID AND L.SALES_DT BETWEEN J1.APPLY_START_DT AND IFNULL(J1.APPLY_END_DT, '99991231')
 AND J1.USE_YN = 'Y'
LEFT JOIN TB_TRAD_MARKET_MERCHANT J2
  ON L.MERCHANT_ID = J2.MERCHANT_ID AND L.SALES_DT BETWEEN J2.APPLY_START_DT AND IFNULL(J2.APPLY_END_DT, '99991231')
 AND J2.USE_YN = 'Y'
LEFT JOIN TB_GENERAL_DEDUCT_MERCHANT J3
  ON L.MERCHANT_ID = J3.MERCHANT_ID AND L.SALES_DT BETWEEN J3.APPLY_START_DT AND IFNULL(J3.APPLY_END_DT, '99991231')
 AND J3.USE_YN = 'Y'
WHERE L.BASE_YM = :base_ym
  AND L.CANCEL_YN = 'N'
  AND (J1.MERCHANT_ID IS NOT NULL OR J2.MERCHANT_ID IS NOT NULL OR J3.MERCHANT_ID IS NOT NULL)
