INSERT INTO TB_DEDUCTION_MONTHLY_SUMMARY (
    CUSTOMER_ID,
    BASE_YM,
    MERCHANT_TYPE,
    TOTAL_AMT,
    TXN_COUNT,
    REG_DTM
)
SELECT
    S.CUSTOMER_ID,
    S.BASE_YM,
    S.MERCHANT_TYPE,
    S.TOTAL_AMT,
    S.TXN_COUNT,
    NOW() AS REG_DTM
FROM (
SELECT
    A.CUSTOMER_ID AS CUSTOMER_ID,
    A.BASE_YM AS BASE_YM,
    CASE
    WHEN B.MERCHANT_ID IS NOT NULL THEN 'BOOK_PERF'
    WHEN T.MERCHANT_ID IS NOT NULL THEN 'TRAD_MARKET'
    WHEN G.MERCHANT_ID IS NOT NULL THEN 'GENERAL'
    ELSE 'UNKNOWN'
END AS MERCHANT_TYPE,
    SUM(A.SALES_AMT) AS TOTAL_AMT,
    COUNT(*) AS TXN_COUNT
FROM TB_CARD_SALES_LEDGER A
LEFT JOIN TB_BOOK_PERF_MERCHANT B
  ON A.MERCHANT_ID = B.MERCHANT_ID AND A.SALES_DT BETWEEN B.APPLY_START_DT AND IFNULL(B.APPLY_END_DT, '99991231')
 AND B.USE_YN = 'Y'
LEFT JOIN TB_TRAD_MARKET_MERCHANT T
  ON A.MERCHANT_ID = T.MERCHANT_ID AND A.SALES_DT BETWEEN T.APPLY_START_DT AND IFNULL(T.APPLY_END_DT, '99991231')
 AND T.USE_YN = 'Y'
LEFT JOIN TB_GENERAL_DEDUCT_MERCHANT G
  ON A.MERCHANT_ID = G.MERCHANT_ID AND A.SALES_DT BETWEEN G.APPLY_START_DT AND IFNULL(G.APPLY_END_DT, '99991231')
 AND G.USE_YN = 'Y'
WHERE A.BASE_YM = :base_ym
  AND A.CANCEL_YN = 'N'
  AND (B.MERCHANT_ID IS NOT NULL OR T.MERCHANT_ID IS NOT NULL OR G.MERCHANT_ID IS NOT NULL)
GROUP BY
    A.CUSTOMER_ID,
    A.BASE_YM,
    CASE
    WHEN B.MERCHANT_ID IS NOT NULL THEN 'BOOK_PERF'
    WHEN T.MERCHANT_ID IS NOT NULL THEN 'TRAD_MARKET'
    WHEN G.MERCHANT_ID IS NOT NULL THEN 'GENERAL'
    ELSE 'UNKNOWN'
END
) S
